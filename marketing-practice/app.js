function normalizeText(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/[.,!?;:"'()[\]{}־\-_/\\|]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function uniqueWords(value) {
  return [...new Set(normalizeText(value).split(" ").filter((word) => word.length > 1))];
}

function scoreText(queryWords, text) {
  const normalized = normalizeText(text);
  return queryWords.reduce((score, word) => score + (normalized.includes(word) ? 1 : 0), 0);
}

function answerMaterialQuestion() {
  const question = els.globalAskInput.value.trim();
  if (!question) {
    els.globalAskAnswer.innerHTML = "<p>כתבו שאלה ואז לחצו על קבלת תשובה. לדוגמה: מה ההבדל בין SWOT ל-PESTLE?</p>";
    return;
  }

  const queryWords = uniqueWords(question);
  const matches = knowledgeBase
    .map((item) => {
      const haystack = [
        item.topic,
        item.keywords.join(" "),
        item.answer.join(" ")
      ].join(" ");
      const keywordBoost = item.keywords.some((keyword) => normalizeText(question).includes(normalizeText(keyword))) ? 4 : 0;
      return { item, score: scoreText(queryWords, haystack) + keywordBoost };
    })
    .filter((match) => match.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 3);

  const relatedTerms = terms
    .map((term) => ({
      term,
      score: scoreText(queryWords, `${term.en} ${term.he} ${term.meaning}`)
    }))
    .filter((match) => match.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 5);

  const relatedQuestions = questions
    .map((item) => ({
      question: item,
      score: scoreText(queryWords, `${item.topic} ${item.text} ${item.explanation}`)
    }))
    .filter((match) => match.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 3);

  if (!matches.length) {
    els.globalAskAnswer.innerHTML = `
      <h3>לא מצאתי התאמה חזקה במאגר</h3>
      <p>נסו לנסח את השאלה עם מושג מהמצגות, למשל Marketing Research (מחקר שיווקי), SWOT, PESTLE, Positioning (מיצוב), Marketing Mix (תמהיל השיווק), IMC או KPI.</p>
    `;
    return;
  }

  const primary = matches[0].item;
  const extraMatches = matches.slice(1).map((match) => `<li>${match.item.topic}</li>`).join("");
  const termList = relatedTerms.map((match) => `<li>${match.term.en} (${match.term.he}) - ${match.term.meaning}</li>`).join("");
  const questionList = relatedQuestions.map((match) => `<li>${match.question.text}</li>`).join("");

  els.globalAskAnswer.innerHTML = `
    <h3>${primary.topic}</h3>
    ${primary.answer.map((paragraph) => `<p>${paragraph}</p>`).join("")}
    ${extraMatches ? `<h4>נושאים קרובים שכדאי לחזור עליהם</h4><ul>${extraMatches}</ul>` : ""}
    ${termList ? `<h4>מושגים קשורים</h4><ul>${termList}</ul>` : ""}
    ${questionList ? `<h4>שאלות תרגול קשורות</h4><ul>${questionList}</ul>` : ""}
  `;
}

